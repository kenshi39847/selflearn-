/*
  変数の宣言
  データ型 変数名 [= 初期値] [,...]
  データ型とは、変数に格納できる値の種類を表す情報である。
  int data;
  上記は「int（整数）値を入れるための、dataという名前の変数を準備する」という意味である。
  また、変数を宣言する際にまとめて初期値を設定することができる。
  int data1 = 108;
  String message = "こんにちは、世界";
*/